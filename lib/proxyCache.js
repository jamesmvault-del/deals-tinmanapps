// backgroundRefresh()
// Called by master-cron.js after updateFeed.js runs.
//
// PURPOSE:
// â€¢ Validate all silo files exist & contain valid JSON
// â€¢ Ensure feed-cache.json exists & is valid
// â€¢ Zero external dependencies
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
export async function backgroundRefresh() {
  console.log("ğŸ” [ProxyCache] Running background integrity syncâ€¦");

  // 1ï¸âƒ£ Validate all category silos
  let total = 0;
  for (const file of CATEGORY_FILES) {
    const data = ensureValidSilo(file);
    total += Array.isArray(data) ? data.length : 0;
  }

  console.log(`âœ… [ProxyCache] All category silos validated (${total} total entries)`);

  // 2ï¸âƒ£ Validate feed-cache.json
  if (!fs.existsSync(FEED_CACHE_PATH)) {
    fs.writeFileSync(FEED_CACHE_PATH, "[]");
    console.log("âš ï¸ [ProxyCache] feed-cache.json missing â†’ created baseline file.");
  } else {
    const parsed = safeRead(FEED_CACHE_PATH);
    if (!Array.isArray(parsed)) {
      fs.writeFileSync(FEED_CACHE_PATH, "[]");
      console.log("âš ï¸ [ProxyCache] feed-cache.json corrupt â†’ repaired.");
    }
  }

  console.log("âœ… [ProxyCache] Background sync complete.");
  return { status: "ok", totalEntries: total };
}

